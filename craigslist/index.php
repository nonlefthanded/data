<h1>Craigslist Gigs</h1>

<p>Use browser to get Craigslist "gigs" search results, in JSON format.

<h2>Sample URL's</h2>
<p>The var "city" needs to be a correct Craigslist city subdomain. The var "query" can be whatever you want...
<ul>
  <li>
    <a href="gigs.php">
      gigs.php (no vars)</a>
    | Portland | Wordpress
  </li>

  <li>
    <a href="gigs.php?city=losangeles&query=react">
      gigs.php?city=losangeles&query=react</a>
    | Los Angeles | React
  </li>

  <li>
    <a href="gigs.php?city=newyork&query=shopify">
      gigs.php?city=losangeles&query=react</a>
    | New York | Shopify
  </li>
</ul>
